function renderWomen(products) {
    let items = "";
    for (const product of products) {
        items += `
        <div class="col">
            <div class="item">
                <img src="${product.image}"
                    alt="">
                <p>${product.name}</p>
                <span class="price-sale">${formatCurrency(product.price)}</span>
            </div>
        </div>
        `
    }
    $(".women-items .row").html(items);
}
