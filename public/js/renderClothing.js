function render_Sale(product) {
    let item = "";
    item += `
    <div class="col">
        <div class="item">
            <img src="${product.image}"
                alt="">
            <p>${product.name}</p>
            <div class="flex-center-center">
                <span class="price">${formatCurrency(product.price)}</span>
                <span class="price-sale">${formatCurrency(product.sale)}</span>
            </div>
        </div>
    </div>
    `
    return item;
}

function render_NotSale(product) {
    let item = "";
    item += `
    <div class="col">
        <div class="item">
            <img src="${product.image}"
                alt="">
            <p>${product.name}</p>
            <span class="price-sale">${formatCurrency(product.price)}</span>
        </div>
    </div>
    `
    return item;
}

function renderMobile(products) {
    let items = "";
    for (const product of products) {
        if (product.sale === 0) {
            items += render_NotSale(product);
        }
        else {
            items += render_Sale(product);
        }
    }
    $(".mobile-items .row").html(items);
}

